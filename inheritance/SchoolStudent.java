package inheritance;

public class SchoolStudent {

	int id;
	String name;
	String sscCertificate;
	
	public SchoolStudent(int id, String name, String sscCertificate) {
		super();
		this.id = id;
		this.name = name;
		this.sscCertificate = sscCertificate;
	}

	public SchoolStudent() {
		super();
	}

}
